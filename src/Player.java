public class Player {
    private int sum = 0;
    private int score = 0;
    private int storedDices = 0;

    public void setSum(int sum) {
       this.sum = sum;
    }

    public int getSum(){
        return this.sum;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int setScore(){
        return this.score;
    }

    public void setStoredDices(int storedDices){
        this.storedDices = storedDices;
    }

    public int getStoredDices() {
        return this.storedDices;
    }


}


